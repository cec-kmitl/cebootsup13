#include <stdio.h>

int main(void)
{
    int a, b, c, h;
    scanf("%d %d %d", &a, &b, &c);
    if (a > b) h = a, a = b, b = h;
    if (a > c) h = a, a = c, c = h;
    if (b > c) h = b, b = c, c = h;
    printf("%d %d %d", a, b, c);
    return 0;
}