#include <stdio.h>

int main(void)
{
    int a, b, odd = 0, even = 0;
    scanf("%d %d", &a, &b);
    for (int i = a; i <= b; i++)
    {
        if (i % 2) odd += i;
        else even += i;
    }
    printf("Sum of odd numbers = %d\nSum of even numbers = %d", odd, even);
    return 0;
}